# Counting one number at a time starting 0 to 50
print("Counting up from 0 to 50:")
for i in range(0, 51, 1):
    print(i)

# One number at a time, counting down from 50 to 0.
print("\nCounting down from 50 to 0:")
for i in range(50, -1, -1):
    print(i)

# Counting up from 30 to 50, one number at a time
print("\nCounting up from 30 to 50:")
for i in range(30, 51, 1):
    print(i)

